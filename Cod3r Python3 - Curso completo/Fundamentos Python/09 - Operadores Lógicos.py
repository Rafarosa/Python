# Operadores Lógicos 

# Tabela verdade

print(True or False)

print(7 != 3 and 2 > 3)

# And
print( True and True)
print( True and False)
print( False and True)
print( False and False)

# or
print( True or True)
print( True or False)
print( False or True)
print( False or False)

# XOR

print( True != True)
print( True != False)
print( False != True)
print( False != False)

# negação
print( not True)
print( not False)
print( not 0)
print( not 1)